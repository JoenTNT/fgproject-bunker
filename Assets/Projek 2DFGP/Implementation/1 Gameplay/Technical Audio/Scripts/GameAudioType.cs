namespace JT.FGP
{
    /// <summary>
    /// Type of audio in game.
    /// </summary>
    public enum GameAudioType
    {
        Master = 0, // Refers to all types of audio.
        BGM = 1, // Refers to background music.
        SFX = 2, // Refers to Sound Effect when action or event is happening.
        Ambience = 3, // Ambience in game, like fog, wind, etc.
    }
}
